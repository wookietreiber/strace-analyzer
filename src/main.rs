use anyhow::Result;

use crate::config::Config;

fn main() -> Result<()> {
    let args = cli::build().get_matches();
    let config = Config::try_from(&args)?;

    // UNWRAP: required via clap
    let input = args.get_one::<String>("input").unwrap();

    analysis::run(input, config)
}
